async function seedCategoryAttributes() {
  try {
    console.log(" Connecting to MongoDB...");
    await mongoose.connect(process.env.MONGO_URI);

    console.log("  Clearing existing attributes...");
    await CategoryAttribute.deleteMany({});

    console.log(" Seeding category attributes...\n");

    let totalAttributes = 0;

    for (const category of CATEGORY_ATTRIBUTES) {
      const result = await CategoryAttribute.create({
        mainCategory: category.mainCategory,
        subCategory: category.subCategory,
        subSubCategory: category.subSubCategory,
        fields: category.fields,
        isActive: true,
      });

      console.log(
        ` ${category.mainCategory} > ${category.subCategory} > ${category.subSubCategory}`
      );
      console.log(`    Fields: ${category.fields.length}`);

      // Count filterable and required fields
      const filterableCount = category.fields.filter((f) => f.filterable).length;
      const requiredCount = category.fields.filter((f) => f.required).length;

      console.log(`    Filterable: ${filterableCount}, Required: ${requiredCount}\n`);

      totalAttributes += category.fields.length;
    }

    console.log("");
    console.log(" CATEGORY ATTRIBUTES SEEDING COMPLETED!");
    console.log("\n");

    console.log(` Summary:`);
    console.log(`    Total Categories with Attributes: ${CATEGORY_ATTRIBUTES.length}`);
    console.log(`    Total Fields: ${totalAttributes}`);
    console.log(`    Average Fields per Category: ${(totalAttributes / CATEGORY_ATTRIBUTES.length).toFixed(1)}\n`);

    // Show stats by data type
    const typeStats = {};
    CATEGORY_ATTRIBUTES.forEach((cat) => {
      cat.fields.forEach((field) => {
        typeStats[field.dataType] = (typeStats[field.dataType] || 0) + 1;
      });
    });

    console.log(` Fields by Data Type:`);
    Object.entries(typeStats).forEach(([type, count]) => {
      console.log(`    ${type}: ${count} fields`);
    });

    // Show filterable stats
    const filterableFields = [];
    const requiredFields = [];
    CATEGORY_ATTRIBUTES.forEach((cat) => {
      cat.fields.forEach((field) => {
        if (field.filterable) filterableFields.push(field.fieldName);
        if (field.required) requiredFields.push(field.fieldName);
      });
    });

    console.log(`\n Filterable Fields: ${filterableFields.length}`);
    console.log(`   Fields: ${filterableFields.join(", ")}`);

    console.log(`\n  Required Fields: ${requiredFields.length}`);
    console.log(`   Fields: ${requiredFields.join(", ")}`);

    console.log("\n Ready to use! Fetch attributes via API:");
    console.log("   GET /api/attributes/category/:mainCategory/:subCategory/:subSubCategory");
    console.log("   GET /api/attributes/filterable/:mainCategory/:subCategory/:subSubCategory\n");

    await mongoose.connection.close();
    process.exit(0);
  } catch (error) {
    console.error(" Error seeding attributes:", error);
    process.exit(1);
  }
}

seedCategoryAttributes();
